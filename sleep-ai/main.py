import random
import re

def find_matching_topics(msg: str) -> list:
    """Находит все подходящие темы по сообщению."""
    msg_lower = msg.lower().strip()
    matched = []
    for topic_name, topic_data in TOPICS.items():
        for keyword in topic_data["keywords"]:
            if keyword in msg_lower:
                matched.append(topic_name)
                break
    return matched

@app.post("/chat", response_model=ChatResponse)
def chat(request: ChatRequest):
    """AI-чат: улучшенный ассистент по сну с контекстом и синонимами."""
    msg = request.message.strip()
    
    if not msg:
        return {"reply": "Напишите ваш вопрос — я помогу! 🌙"}
    
    matched_topics = find_matching_topics(msg)
    
    if matched_topics:
        # Если нашли несколько тем — комбинируем
        if len(matched_topics) > 1:
            parts = []
            for topic in matched_topics[:2]:  # макс 2 темы
                responses = TOPICS[topic]["responses"]
                parts.append(random.choice(responses))
            return {"reply": parts[0] + "\n\n---\n\n" + parts[1]}
        else:
            responses = TOPICS[matched_topics[0]]["responses"]
            return {"reply": random.choice(responses)}
    
    # Не нашли прямого совпадения — даём умный follow-up
    return {"reply": random.choice(FOLLOW_UPS)}
